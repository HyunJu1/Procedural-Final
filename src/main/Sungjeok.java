package main;

public class Sungjeok {
private String gwamokName;
private String gangjwaName;
private int year;
private int semester;
private int seongjeok;
private String grade;


public String getGwamokName() {
		return gwamokName;
	}
	public void setGwamokName(String gwamokName) {
		this.gwamokName = gwamokName;
	}
	public String getGangjwaName() {
		return gangjwaName;
	}
	public void setGangjwaName(String gangjwaName) {
		this.gangjwaName = gangjwaName;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getSemester() {
		return semester;
	}
	public void setSemester(int semester) {
		this.semester = semester;
	}
	public int getSeongjeok() {
		return seongjeok;
	}
	public void setSeongjeok(int seongjeok) {
		this.seongjeok = seongjeok;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
}
